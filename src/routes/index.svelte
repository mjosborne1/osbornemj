  <h1 class="article-title">Career highlights</h1>

  <h3>Scientist at QML Pathology</h3>
  <section>
  After completing a B.App.Sci (Medical Laboratory Science) at QIT (now QUT) in 1987 , I began a job as a Relieving Scientist at QML Pathology in Brisbane Qld. While training in many disciplines at QML's West End laboratory, I completed my Grad. Dip. (Computer Science) at QUT. I spent 8 years as a country reliever at QML before settling down to work at QML's Springwood (Logan) laboratory in 1996.

  </section>
  <br>
  <h3>Programmer/Lead Developer at QML Pathology</h3>
  <section>
  In 1999, a developer position became available at QML West End. I learnt off the best. My mentors were Kevin Walker (a self taught genius who designed and wrote most of the previous Concurrent based system in Assembly language and Fortran), Bruce Harper (a guru in the C language as well as being the in-house Unify database expert ) and John Herron (father of the PIT standard and HL7 standards expert/committee member). At QML, I built on my skills in Unix and Linux scripting and programming. Shell and Perl became my go-to languages to get stuff done. When Kevin was promoted to IT Operations Manager in 2003, I became the Development lead at QML. In the Information Systems Department (ISD) I was involved in lab system upgrades, system configuration of test panels, HL7, worklists, interfaces to analysers and data retrieval and reporting. I was also heavily involved in system integration with the other Primary Health Ltd sites using HL7 version 2 messaging. 
  </section>
 <br>
  <h3>Pathology Technical Lead at Mater Health Services</h3>
  <section>
  I began as a computer scientist learning the Kestral Pathology LIS (PLS) in February 2009. I was on a temporary 12 month contract as the Manager had gone on long service leave and didn't look to be coming back. At Mater I was involved less with the programming (although there are elements to it in PLS and HL7Connect) and more with system configuration. There was still a lot of HL7 integration work to do. I was made permanent full-time in 2010. We went through quite a few managers. I even spent about 12 months as manager of Pathology Information Systems although this was in addition to my team leader / technical role, which was unsustainable. In about 2014 the DIST (Digital Imaging Support Team) and Pathology Information systems team merged to become Diagnostic Solutions, which fit into the Health Solutions team. I basically lead the Pathology Support team from the technical aspect, looking after server support of PLS, HL7Connect, the pathology Intersystems Ensemble production, PKS Rippledown and a few other systems. 
  </section>
 <br>
  <h3>Senior Application Specialist</h3>
  <section>
  Under new leadership, I have moved into a new team, responsible for Pathology Informations Systems, Mater Clinical Portals and Onbase.
  I still work most of the time on Pathology related duties, though I do get pulled in to other projects, mainly in the Integration space.
  2021 will see me come full circle, with Mater Pathology implementing the Ultra LIS, which I spent 15 years or so using, and later, developing at QML.
  </section>
